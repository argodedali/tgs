<h2>VISI</h2>
Menjadi sekolah unggul dalam prestasi, berakhlak mulia,<br> 
dan berwawasan lingkungan.<br>
 <h2>MISI</h2>
1.  Membina mental dan budi pekerti luhur.<br>
2. Melaksanakan kegiatan pembelajaran secara : aktif, kreatif, inovatif, dan menyenangkan.<br>
3. Menumbuhkan sikap komunikatif, koordinatif, dan sportifitas.<br>
4. Melaksanakan pembelajaran lingkungan hidup sehingga mewujudkan sekolah yang sejuk, nyaman, dan sehat untuk belajar<br>